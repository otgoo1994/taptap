<script>
import { message } from 'ant-design-vue';
export default {
  data() {
    return {
      capsWord: true,
      capslock: false,
      shiftLeftkey: false,
      shiftRightkey: false,
      enterkey: false,
      tabkey: false
    }
  },
  computed: {
    caps() { return this.capsWord; },
    shiftLeft() { return this.shiftLeftkey; },
    shiftRight() { return this.shiftRightkey; },
    enter() { return this.enterkey; },
    tab() { return this.tabkey; }
  },
  props: {
    selector: null,
    hand: null
  },    
  methods: {
    keyup(evt) {
        if (evt.key === 'Shift') {
            if (evt.code === 'ShiftLeft') {
                this.shiftLeftkey = false;
            } else {
                this.shiftRightkey = false;
            }
        }

        if (evt.key === 'Enter') {
            this.enterkey = false;
        }

        if (evt.key === 'Tab') {
            evt.preventDefault();
            this.tabkey = false;
        }

        // if (!this.shiftLeftkey && !this.shiftRightkey) {
        //     this.capsWord = !evt.getModifierState('CapsLock')
        // }

        this.capsWord = !evt.getModifierState('CapsLock')
        this.capslock = evt.getModifierState('CapsLock')

        if (this.capslock) {
            message.warning({ content: 'Caps Lock is ON', key: 'updatable' });
        }
    },
    keydown(evt) {
        if (evt.key === 'Tab') {
            evt.preventDefault();
            this.tabkey = true;
        }

        if (evt.key === 'Shift') {
            if (evt.code === 'ShiftLeft') {
                this.shiftLeftkey = true;  
            } else {
                this.shiftRightkey = true;
            }

            if (evt.getModifierState('CapsLock')) {
                this.capsWord = true;
            } else {
                this.capsWord = false;
            }
        }

        if (evt.key === 'Enter') {
            this.enterkey = true;
        }
    },
  },
  watch: {
    hand: function() {
        console.log(this.capslock);
    }
  },
  mounted(e) {
    window.addEventListener('keyup', this.keyup);
    window.addEventListener('keydown', this.keydown);
  },
}
